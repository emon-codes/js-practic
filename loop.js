
for (let i = 1; i <= 10; i++){
    console.log(i)
}


let count = 1;

while (count <= 20) {
    console.log(count);
    count++
}



let counts = 1;

do {
    console.log(counts);
    counts++;
} while (count <= 5);



// for loop ব্যবহার করা হয় যখন iteration সংখ্যা জানা থাকে

// while loop ব্যবহার করা হয় যখন iteration সংখ্যা জানা থাকে না

// do...while loop ব্যবহার করা হয় যখন কমপক্ষে একবার execute করতেই হয়
